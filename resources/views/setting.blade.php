<div style="text-align: justify">
    <p>With the fast development of new mobile technology, 5G is becoming more readily available as the foundation for
        unleashing a broad set of innovative applications and a variety of industrial verticals, e.g., industry 4.0,
        healthcare and transportation. In the era of 5G, the networks are becoming increasingly complex systems due to
        the significant revolution of hardware and software techniques. We are also witnessing a transformation in the
        way networks are operated, shifting from Quality of Service (QoS)-centric to Quality of Experience (QoE)-centric
        and aiming to offer differentiated and personal services to customers. These changes require networks to be
        managed and operated in a smarter and more agile manner, incorporating computational intelligence in networks,
        edge/cloud computing and services.

        SmartCNS-2019 aims to address the important problems and challenges caused by the network complexity, ultra-high
        QoE demands and diverse services. The conference provides a leading-edge forum to bring together scientists,
        researchers and engineers in different disciplines to share their experience and ideas and exchange their novel
        work regarding the state-of-the-art and in-progress research on all aspects of smart computing, networking and
        services.

        SmartCNS-2019 will be held on 21-23 October 2019 in Shenyang, China. It was previously held as SmartNEM-2018
        (Exeter, UK, June 2018) WNM-2017 (Exeter, UK, June 2017), WNM-2016 (Sydney, Australia, December 2016), WNM-2015
        (Liverpool, UK, October 2015), WNM-2014 (Dalian, China, August 2014), WNM-2013 (Melbourne, Australia, July
        2013), WNM-2012 (Liverpool, UK, June 2012), WNM-2011 (Changsha, China, November 2011), and HMWN-2010 (Bradford,
        UK, June 2010).

